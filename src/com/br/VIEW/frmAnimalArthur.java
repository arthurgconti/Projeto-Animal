    private void Cadastrar(){
        
        try{
            AnimalDTOArthur animal = new AnimalDTOArthur();
            AnimalDAOArthur animaldao = new AnimalDAOArthur();
            
            animal.setNome(txtNome.getText());
            animal.setDataAdocao(txtAdocao.getText());
            animal.setTamanho(cbxTamanho.getSelectedItem().toString());
            animal.setFase(cbxFase.getSelectedItem().toString());
            animal.setCodDono(idDono.get(cbxDono.getSelectedIndex()-1));
            animal.setCodRaca(idRaca.get(cbxRaca.getSelectedIndex()-1));
            
            if(animaldao.inserirAnimal(animal)==true){
            JOptionPane.showMessageDialog(null,"Cadastro efetuado com sucesso!");
            Limpar();
            preencheBusca();
            }         
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(),"Erro ao alterar animal"
          ,JOptionPane.ERROR_MESSAGE);               
        }
    }
    
