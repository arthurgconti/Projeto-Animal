    private void setarCampos(){
        try{

        AnimalDTOArthur animal = new AnimalDTOArthur();
        AnimalDAOArthur animaldao = new AnimalDAOArthur();
        
        int setar = tblAnimal.getSelectedRow();
        lblId.setText(tblAnimal.getModel().getValueAt(setar,0).toString());
        animal.setIdAnimal(Integer.parseInt(lblId.getText()));
        
        ResultSet rs = animaldao.selecionaCampos(animal);
        
        while(rs.next()){
            animal.setFase(rs.getString("fase"));
            animal.setTamanho(rs.getString("tamanho"));
        }
        
        
        txtNome.setText(tblAnimal.getModel().getValueAt(setar, 1).toString());
        cbxDono.setSelectedItem(tblAnimal.getModel().getValueAt(setar,2));
        cbxRaca.setSelectedItem(tblAnimal.getModel().getValueAt(setar,3));
        txtAdocao.setText(tblAnimal.getModel().getValueAt(setar,4).toString());
        cbxFase.setSelectedItem(animal.getFase());
        cbxTamanho.setSelectedItem(animal.getTamanho());
        }
        catch(Exception e){
          JOptionPane.showMessageDialog(null, e.getMessage(),""
           ,JOptionPane.ERROR_MESSAGE);                           
        }
    }
    
    
