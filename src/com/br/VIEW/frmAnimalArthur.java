    private void preencheBusca(){
        try{
            AnimalDTOArthur animal = new AnimalDTOArthur();
            AnimalDAOArthur animaldao = new AnimalDAOArthur();
            
        
        ResultSet rsAnimal = animaldao.listaBusca();
        
        String[] todos={"Id","Nome","Dono","Raça","Data da Adoção"};
        String[][] linha = {};
        
        DefaultTableModel tablemodel = new DefaultTableModel(linha,todos){
                            public boolean isCellEditable(int row,int col){
                        return false;
            }
        }; 
        
        Vector<Vector> dados = new Vector<>();
        
        while(rsAnimal.next()){
            Vector regVector = new Vector();
            regVector.add(rsAnimal.getInt("a.idanimal"));
            regVector.add(rsAnimal.getString("a.nome"));
            regVector.add(rsAnimal.getString("d.nome"));
            regVector.add(rsAnimal.getString("r.nome"));
            regVector.add(rsAnimal.getString("a.dataadocao"));
            
            dados.add(regVector);
            tablemodel.addRow(regVector);
        }
            tblAnimal.setModel(tablemodel);
            tblAnimal.getTableHeader().setReorderingAllowed(false);
            
            
        }catch(Exception e){
          JOptionPane.showMessageDialog(null, e.getMessage(),""
           ,JOptionPane.ERROR_MESSAGE);            
        }
    }
    
    Vector<Integer> idDono = new Vector<>();
    Vector<Integer> idRaca = new Vector<>();

    private void restaurarDadosDono(){
        try{
            AnimalDAOArthur animaldao = new AnimalDAOArthur();
            
            ResultSet rs = animaldao.pesquisaDono();
            
            while(rs.next()){
                idDono.addElement(rs.getInt(1));
                cbxDono.addItem(rs.getString(2));
            }
            
            
        }catch(Exception e){
          JOptionPane.showMessageDialog(null, e.getMessage(),""
           ,JOptionPane.ERROR_MESSAGE);                     
        }
    }
    
